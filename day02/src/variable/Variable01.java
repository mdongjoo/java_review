package variable; //패키지명 variable

public class Variable01 { //클래스 Variable01 선언 부분 
	public static void main(String[] args) { //메인메소드 선언부분
		int num = 10 ; //정수형 변수 선언 초기화 
		int num2 = 20 ; //정수형 변수 선언 초기화 
		System.out.println(num2); //정수값 출력 줄바꿈 
		
		int number1 = 0, number2 = 100, number3 = 200; //연속으로 ,을 이용해 선언및 초기화 
		System.out.println(number1); //정수형 변수 3개 출력 
		System.out.println(number2);
		System.out.println(number3);
		//자동 정렬 : ctrl + shift + f 
		//한줄 복사 : ctrl + alt + 방향키 위아래
		//여러줄 복사 : shift + 방향키 범위 지정 -> ctrl + alt + 방향키 위아래 
	}
}
