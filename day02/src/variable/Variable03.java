package variable; //패키지명

public class Variable03 { //클래스 선언부
	public static void main(String[] args) { //메인메소드 선언부 
		String name = "문동주"; //문자열 변수 선언 및 초기화 
		int age = 25 ; //정수형 변수 선언 및 초기화 
		System.out.println(name ); //출력문 
		System.out.println(age); //출력문 
		System.out.println(" 제 이름은 "+ name + "이고 제 나이는 " + age + "살 입니다" ); //문자열 형태로 출력 "" + @
		
	}//메인 메소드 종료
} //클래스 종료 
